#include <iostream>

using namespace std;

int main () {
    int a = 5;               // Integer (whole number)
    float b = 5.99;     // Floating point number
    double c = 9.98;   // Floating point number
    char d = 'D';         // Character
    bool e = true;       // Boolean
    string f = "Hello";     // String

    cout << a << " " << b << " " << c << " " << d << " " << e << endl;
    return 0;
}